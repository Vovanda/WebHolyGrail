import type { MediaRef } from './media';

/**
 * Регалии собаки — одна строка в списке достижений (`titles[]` у {@link DogDoc}).
 *
 * @remarks
 * Не enum — у каждого титула в РКФ/FCI свой шифр и формулировка (CACIB, CW,
 * Чемпион РФ, J.CH., интерчемпион, рабочий класс, IPO, BH, ZTP, KKL и т.д.).
 * Заводчик сам формулирует текст, поэтому это просто `text` с опциональным
 * годом/местом.
 */
export interface DogTitle {
  /** Сама регалия — «Чемпион России», «CACIB Москва», «КС-1». */
  readonly text: string;
  /** Год получения, для сортировки и подписей. */
  readonly year?: number;
  /** Место / организация — «Москва, Евразия 2024», «РКФ-FCI». */
  readonly place?: string;
}

/** Пол собаки. */
export type DogSex = 'male' | 'female';

/**
 * Окрас восточноевропейской овчарки. Не строгий enum — для других пород значения
 * другие; на старте достаточно набора ВЕО, при расширении переедет в Globals.
 */
export type DogColor = 'cheprachny' | 'zonarny' | 'cherny';

/**
 * Публичная форма собаки (производителя / выпускника / щенка-подростка
 * оставшегося в питомнике). Источник правды — коллекция `Dogs` в Payload.
 *
 * @remarks
 * **Что НЕ Dog:** щенок из активного помёта на витрине — это {@link import('./litters').Puppy}
 * внутри {@link import('./litters').LitterDoc}, **не** запись в Dogs. Запись в
 * Dogs появляется только когда щенок остался в питомнике как производитель или
 * показывается на сайте как самостоятельная сущность с собственной карточкой.
 *
 * Это снимает дублирование: 8 щенков одного помёта **не** плодят 8 записей Dogs
 * с одинаковыми мама/папа.
 */
export interface DogDoc {
  readonly id: string;
  /** ЧПУ-slug: `/dog/<slug>`. */
  readonly slug: string;
  /** Кличка для админки и заголовка карточки. */
  readonly name: string;
  /**
   * Прозвища и сокращения для auto-highlight в VK-постах.
   * Например: «Марта» для «ОМСКАЯ ДРУЖИНА МАРТА». Регистр игнорируется.
   * Не показываются как отдельные клички — только триггеры подсветки.
   */
  readonly aliases?: ReadonlyArray<{ readonly id: string; readonly alias: string }>;
  /** Пол. */
  readonly sex: DogSex;
  /** Дата рождения ISO `YYYY-MM-DD` (не timestamp — час рождения не нужен). */
  readonly dob?: string;
  /** Окрас. */
  readonly color?: DogColor;
  /**
   * Фотографии собаки в порядке отображения. Первая = главная.
   *
   * Форма `{ id, image }` — это Payload array-row: каждая строка имеет
   * автогенерируемый `id`, а само медиа лежит в поле `image`. Так же сделан
   * `LitterDoc.images` — единый паттерн для галерей в коллекциях.
   */
  readonly photos?: readonly { readonly id: string; readonly image: MediaRef }[];
  /**
   * Регалии — массив отдельных строк. Порядок отображения = порядок в массиве
   * (заводчик сам решает что вперёд: свежее или весомое).
   */
  readonly titles?: readonly DogTitle[];
  /**
   * Описание собаки — rich text Lexical-сериализация (JSON-объект).
   * На клиенте рендерится через `<RichText>` из @payloadcms/richtext-lexical.
   */
  readonly description?: unknown;
  /** Ссылка на родословную (PDF / страница в РКФ-базе). */
  readonly pedigreeUrl?: string;
  /**
   * РКФ-id собаки в `veorkf.ru/catalog/dog.php?id=N` — нужен для импорта
   * родословной из РКФ и для линковки наших Dog с записями там.
   */
  readonly rkfId?: number;
  /**
   * Питомник собаки. Заполняется только когда собака **не** из «Омской Дружины»
   * (привозной производитель — отец помёта). Если пусто — собака считается
   * нашей и поле в карточке не показывается.
   */
  readonly kennel?: string;
  /** ФИО заводчика. Заполняется когда отличается от владельца питомника. */
  readonly breeder?: string;
  /**
   * Родословная — плоский массив по позициям binary-heap layout'а РКФ
   * (`dog-data.php` отдаёт ровно такую структуру):
   *
   * |  pos | роль                                     |
   * |------|------------------------------------------|
   * |   1  | отец                                     |
   * |   2  | дед по отцу                              |
   * |  3-4 | прадеды линии отца-отца                  |
   * |   5  | бабка по отцу                            |
   * |  6-7 | прадеды линии отца-матери                |
   * |   8  | мать                                     |
   * |   9  | дед по матери                            |
   * | 10-11| прадеды линии матери-отца                |
   * |  12  | бабка по матери                          |
   * | 13-14| прадеды линии матери-матери              |
   *
   * Пустые позиции просто отсутствуют в массиве (не хранятся null'ами).
   * Глубина 3 поколения (родители + деды + прадеды) — как в legacy.
   */
  readonly pedigree?: readonly PedigreeAncestor[];
  readonly updatedAt: string;
}

/**
 * Один предок в родословной. Сохраняется при импорте Dog'а из РКФ. Не
 * пытаемся резолвить в `DogDoc` (предки в подавляющем большинстве — НЕ наши
 * собаки, их нет в Payload). Кликабельность: если по `rkfId` найдётся наша
 * Dog — ссылаемся на `/dog/<slug>`, иначе на live-proxy `/catalog/<rkfId>`.
 */
export interface PedigreeAncestor {
  /** Позиция в heap 1..14 (см. таблицу в `DogDoc.pedigree`). */
  readonly position: number;
  /** РКФ-id предка для линковки на РКФ-карточку. */
  readonly rkfId?: number;
  /** Имя предка как в РКФ (uppercase). */
  readonly name: string;
  /** Заметка — обычно «{окрас}, {год}», иногда «{окрас}, {год} {регион}». */
  readonly note?: string;
}
